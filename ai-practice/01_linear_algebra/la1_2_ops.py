"""
[실전선대] §1.2 — 벡터 연산: 덧셈/뺄셈·전치·브로드캐스팅·스칼라 곱·평균
(03-MD와 겹치는 덧셈/스케일링 외에 '새 개념' 위주: 뺄셈·전치·broadcasting·평균)
실행: python la1_2_ops.py
"""
import numpy as np

# ── §1.2.1 덧셈/뺄셈 (원소별, 같은 차원만) ────────────────────
v = np.array([4, 5, 6]); w = np.array([10, 20, 30])
print("v+w =", v + w, "| v-w =", v - w)          # [14 25 36] [-6 -15 -24]
try:
    _ = v + np.array([0, 3, 6, 9])               # 차원 불일치
except ValueError as e:
    print("차원 불일치 차단:", str(e)[:40])

# ── §1.2.1 방향이 다르면? 열+행 = 브로드캐스팅으로 '행렬' ──────
col = np.array([[4, 5, 6]]).T                     # 열벡터 (3,1)
row = np.array([[10, 20, 30]])                    # 행벡터 (1,3)
print("열+행 결과 shape:", (col + row).shape)     # (3,3) ← 방향 다르면 행렬!
print(col + row)

# ── §1.2.5 전치 (transpose): vᵀᵀ = v ─────────────────────────
print("vᵀ shape:", row.T.shape, "| vᵀᵀ==v:", np.array_equal(row.T.T, row))

# ── §1.2.3 스칼라-벡터 곱 (λw) ────────────────────────────────
lam, ww = 4, np.array([9, 4, 1])
print("λw =", lam * ww)                           # [36 16 4]

# ── §1.2.4 스칼라-벡터 덧셈 (수학적 불가, 파이썬 broadcasting) ─
print("s+v (broadcasting) =", 2 + np.array([3, 6]))  # [5 8]

# ── §1.2.6 브로드캐스팅 덧셈 (행+열) ──────────────────────────
print("브로드캐스팅 [1,2,3]ᵀ + [10,20]:\n",
      np.array([[1, 2, 3]]).T + np.array([[10, 20]]))  # 3x2

# ── 벡터 평균 = 합 × (1/N) ────────────────────────────────────
a, b = np.array([2, 8]), np.array([4, 2])
print("두 벡터 평균:", (a + b) * (1 / 2))         # [3. 5.]

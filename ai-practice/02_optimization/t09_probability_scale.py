"""T09 — 확률은 왜 0.0~1.0인가: 부동소수점 팩트체크 + 로그 공간.

검증 목표
  1) Gemini 주장 반증: 상대 정밀도는 1.0과 100.0에서 동일 (유효숫자 손실 없음)
  2) 0~100 스케일의 실제 해악: x*100/100 왕복 손상률 (~14.6%)
  3) 확률 곱 연쇄의 언더플로 붕괴 vs 로그 공간 생존
연결 노트: 탐구/T09, 연습/P07(행렬식 로그 실험)
"""
import math
import random

import numpy as np

# ── 1. 절대 간격(ULP)은 다르지만, 상대 정밀도는 동일 ──────────────
print("ULP(0.5)   =", np.spacing(0.5))     # 1.11e-16
print("ULP(1.0)   =", np.spacing(1.0))     # 2.22e-16 (= 머신 엡실론)
print("ULP(100.0) =", np.spacing(100.0))   # 1.42e-14 ← 절대 간격은 640배 큼
print("상대 간격 1.0  :", np.spacing(1.0) / 1.0)      # 2.22e-16
print("상대 간격 100.0:", np.spacing(100.0) / 100.0)  # 1.42e-16 ← 사실상 동일!
# → "100처럼 커지면 유효숫자 손실 시작"은 오류. 지수·가수 필드는 분리되어 있다.

# ── 2. 0~100 스케일의 왕복 손상 (100은 2의 거듭제곱이 아님) ────────
random.seed(0)
n = 100_000
bad = sum((x := random.random()) != x * 100 / 100 for _ in range(n))
print(f"x*100/100 != x 비율: {bad / n:.4f}")  # ≈ 0.146

# 반전: 정수 0..100은 float로 전부 정확, 십진 소수 0.05는 부정확
print("0.05 저장값:", format(0.05, ".20f"))   # 0.05000000000000000278
print("5.0  저장값:", format(5.0, ".20f"))    # 정확

# ── 3. 언더플로 붕괴 vs 로그 공간 ─────────────────────────────────
p = 1.0
for _ in range(400):
    p *= 0.01                                # 참값 1e-800 < 4.9e-324(최소 서브노멀)
print("선형 곱 0.01^400 :", p)               # 0.0 — 붕괴
log_p = sum(math.log(0.01) for _ in range(400))
print("로그 합           :", log_p)          # -1842.068... (= 400·ln0.01, 정확)
print("이론값            :", 400 * math.log(0.01))
